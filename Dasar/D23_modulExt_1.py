def tambah(a,b) :
    hasil = a + b
    print("{} + {} = {}".format(a,b,hasil))

def kurang(a,b) :
    hasil = a - b
    print("{} - {} = {}".format(a,b,hasil))

def kali(a,b) :
    hasil = a * b
    print("{} + {} = {}".format(a,b,hasil))

def bagi(a,b) :
    hasil = a / b
    print("{} / {} = {}".format(a,b,hasil))

def modulus(a,b) :
    hasil = a % b
    print("{} % {} = {}".format(a,b,hasil))

